// Package singleflight provides a duplicate function call suppression
// mechanism.
package singleflight

import "sync"

// call is an in-flight or completed Do call
type call struct {
	wg  sync.WaitGroup
	val interface{}
	err error
}

// Group represents a class of work and forms a namespace in which
// units of work can be executed with duplicate suppression.
type Group struct {
	mu sync.Mutex       // protects m
	m  map[string]*call // lazily initialized
}

// Do executes and returns the results of the given function, making
// sure that only one execution is in-flight for a given key at a
// time. If a duplicate comes in, the duplicate caller waits for the
// original to complete and receives the same results.
// Do 方法实现的原理就是，一个请求过来的时候，首先查看 Group 对象的 map 中是否存在相同的请求，如果有，则使用该请求的 WaitGroup 对象进行等待，如果没有，则在请求开始的时候，加入到 map 中，并使用 WaitGroup 对象的 Add 方法使相同的请求进行阻塞，接着，开始执行传入的回调函数，执行完毕后，赋值给 call 对象，并使用 WaitGroup 对象的 Done 方法，表明请求结束，此时，如果有相同的请求的 Wait 方法就会接触阻塞，最后，再从 map 中删除该请求即可。
func (g *Group) Do(key string, fn func() (interface{}, error)) (interface{}, error) {
	g.mu.Lock()
	if g.m == nil {
		g.m = make(map[string]*call)
	}
	if c, ok := g.m[key]; ok {
		g.mu.Unlock()
		c.wg.Wait()
		return c.val, c.err
	}
	c := new(call)
	c.wg.Add(1)
	g.m[key] = c
	g.mu.Unlock()

	c.val, c.err = fn()
	c.wg.Done()

	g.mu.Lock()
	delete(g.m, key)
	g.mu.Unlock()

	return c.val, c.err
}
